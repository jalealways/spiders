    def start_requests(self):
        # 车型
        yield Request(self.pinpai_url, callback=self.parse_pinpai)
        # 问答 最新
        yield Request(self.wenda_url, callback=self.parse_wenda_list, headers=self.head,
                      body=self.wenda_body.format(0), method='Post', meta={'entry_id': 48041})
        # 兴趣社区
        for i in self.xingqu_shequ['forumid']:
            yield Request(self.chexingshequ_list_url.format(i, 0), callback=self.parse_list, meta={'forumId': i})
        # 测试
        # yield Request(self.chexingshequ_list_url.format(230, 0), callback=self.parse_list, meta={'forumId': 230})

    def parse_pinpai(self, response):
        data = json.loads(response.body)['data']
        for i in data:
            pinpai_id = i['masterId']
            yield Request(self.pipai_list_url.format(pinpai_id), callback=self.parse_chexing)

    def parse_chexing(self, response):
        data = json.loads(response.body)['data']
        for i in data:
            forumid = i['forumId']
            yield Request(self.chexingshequ_list_url.format(forumid, 0, 0), callback=self.parse_list,
                          meta={'forumId': forumid})

    def parse_wenda_list(self, response):
        logger.info("Wenda_List url: %s" % response.url)
        data = json.loads(response.body)['data']
        for i in data['list']:
            topicid = i['topicId']
            yield Request(self.detail_url.format(topicid), callback=self.parse_detail, meta=response.meta)
        # 分页
        dest_time = data['list'][-1]['publishTime']
        flag = self.cal_time(dest_time)
        if flag:
            minid = data['list'][-1]['topicId']
            yield Request(self.wenda_url, callback=self.parse_wenda_list, headers=self.head,
                          body=self.wenda_body.format(minid), method='Post', meta=response.meta)

    def parse_list(self, response):
        logger.info("List url: %s" % response.url)
        data = json.loads(response.body)["data"]
        forumid = response.meta['forumId']
        for i in data['list']:
            topicid = i['topicId']
            yield Request(self.detail_url.format(topicid), callback=self.parse_detail)
        # 分页
        try:
            dest_time = data['list'][-1]['publishTime']
            flag = self.cal_time(dest_time)
            if flag:
                minid = data['list'][-1]['topicId']
                yield Request(self.chexingshequ_list_url.format(forumid, minid, 0),
                              callback=self.parse_list, meta={'forumId': forumid})
        except Exception as e:
            logger.info('没有分页, e.message: %s' % e)

